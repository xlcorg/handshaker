use tauri_specta::{collect_commands, collect_events, Builder};

/// Build a `tauri_specta::Builder` populated with every command and event the
/// app exposes. Used by both `run()` and the `export-bindings` helper binary.
pub fn specta_builder() -> Builder<tauri::Wry> {
    Builder::<tauri::Wry>::new()
        .commands(collect_commands![
            app_version,
            grpc_describe,
            grpc_refresh_contract,
            grpc_build_request_skeleton,
            grpc_invoke_oneshot,
            env_list,
            env_active_get,
            env_active_set,
            env_upsert,
            env_delete,
            vars_resolve,
            collection_list,
            collection_get,
            collection_upsert,
            collection_delete,
            collection_set_variables,
            collection_add_item,
            collection_rename_item,
            collection_move_item,
            collection_duplicate_item,
            collection_delete_item,
            collection_restore_item,
            auth_set_for_env,
        ])
        .events(collect_events![ContractUpdated])
}

#[cfg_attr(mobile, tauri::mobile_entry_point)]
pub fn run() {
    // Install rustls' default crypto provider once at process start. tonic 0.14 with
    // `tls-ring` does NOT install it automatically; without this the first TLS handshake
    // panics with "no process-level CryptoProvider available".
    rustls::crypto::ring::default_provider()
        .install_default()
        .expect("install rustls ring provider");

    let specta_builder = specta_builder();

    #[cfg(debug_assertions)]
    {
        let out = std::path::Path::new(env!("CARGO_MANIFEST_DIR"))
            .join("..")
            .join("src")
            .join("ipc")
            .join("bindings.ts");
        specta_builder
            .export(
                Typescript::default()
                    .formatter(specta_typescript::formatter::prettier)
                    .header(
                        "// @ts-nocheck\n\
                         // AUTO-GENERATED by tauri-specta. Do NOT edit.\n",
                    ),
                &out,
            )
            .expect("failed to export tauri-specta bindings");
    }

    tauri::Builder::default()
        .invoke_handler(specta_builder.invoke_handler())
        .setup(move |app| {
            specta_builder.mount_events(app);
            let data_dir = app
                .path()
                .app_data_dir()
                .expect("resolve app_data_dir");
            let state = AppState::load(&data_dir)
                .expect("initialize AppState from data dir");
            app.manage(state);
            Ok(())
        })
        .run(tauri::generate_context!())
        .expect("error while running tauri application");
}
